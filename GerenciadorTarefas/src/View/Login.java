    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
          String senha = new String(txtSenha.getPassword()).trim();
          String usuarioLogado = txtUsuario.getText();
          usuarios = usuariosDAO.getUsuario(usuarioLogado);
          
       
    if(usuarios.getSenha() != null && usuarios.getSenha().equals(senha)){
          
        new GeradorTarefas().setVisible(true);
        dispose();
    } else {
               JOptionPane.showMessageDialog(null,"Por favor, preencha o campo de usuario e senha corretamente!");

      }
        
    }//GEN-LAST:event_btnEntrarActionPerformed

